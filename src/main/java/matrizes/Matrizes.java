package matrizes;

public class Matrizes {
    public static void main(String[] args) {
        int [] numbers = new int[3];
        //pelo q entendi, ao criar uma matriz, no passamos o tipo, e dizemos quantos
        // objetos o array possui, e atribuímos o valor em cada objeto chamando a
        // variável e o numero da sua posição no array
        numbers[0] = 1;
        numbers[1] = 10;
        numbers[2] = 100;
        System.out.println(numbers[0]);
        System.out.println(numbers[1]);
        System.out.println(numbers[2]);


    }
}
