package variaveis;

public class Variaveis {
    public static void main(String[] args) {
        /*
        * Para declarar uma variável em Java, devemos especificar seu nome
        * (também chamado de identificador) e tipo.         *
        * */

        int a = 10;
        long b = 20;
        byte c = 30;
        short d = 40;

        float e = 1.2f;
        double f = 1.1f;

        char g = 'A'; // a variável char deve ser usada com aspas simples

        boolean h = true;
    }
}
